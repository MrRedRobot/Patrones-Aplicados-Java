public class Combo {
    
    private PlatoPrincipal platoPrincipal;
    private Acompañamiento acompañamiento;
    private Bebida bebida;
    private Postre postre;

    public PlatoPrincipal getPlatoPrincipal() {
        return platoPrincipal;
    }

    public void setPlatoPrincipal(PlatoPrincipal platoPrincipal) {
        this.platoPrincipal = platoPrincipal;
    }

    public Acompañamiento getAcompañamiento() {
        return acompañamiento;
    }

    public void setAcompañamiento(Acompañamiento acompañamiento) {
        this.acompañamiento = acompañamiento;
    }

    public Bebida getBebida() {
        return bebida;
    }

    public void setBebida(Bebida bebida) {
        this.bebida = bebida;
    }

    public Postre getPostre() {
        return postre;
    }

    public void setPostre(Postre postre) {
        this.postre = postre;
    } 
}
